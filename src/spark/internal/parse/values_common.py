from spark.internal.version import *
################################################################
# List

List = tuple

def isList(x):
    return isinstance(x, tuple)
    
def createList(*args):
    return args

EMPTY_SPARK_LIST = List(())

################################################################
# String

String = str

def isString(x):
    return isinstance(x, basestring)

################################################################
# Boolean

Boolean = bool

def isBoolean(x):
    return isinstance(x, bool)

################################################################
# Integer, Float

def Integer(x):
    try:
        return int(x)
    except ValueError:
        return long(x)

def isInteger(x):
    return isinstance(x, _INT_OR_LONG)

Float = float

def isFloat(x):
    return isinstance(x, float)

_INT_OR_LONG = (int, long)
_INT_OR_LONG_OR_FLOAT = (int, long, float)

def isNumber(x):
    return isinstance(x, _INT_OR_LONG_OR_FLOAT)
